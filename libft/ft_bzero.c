#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmps;
	int				i;

	tmps = s;
	i = 0;
	while ((size_t)i < n)
	{
		tmps[i] = '\0';
		i++;
	}
}
